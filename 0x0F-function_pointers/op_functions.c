#include "calc.h"
#include <stdio.h>
#include <stdlib.h>

/**
 * op_add - adds
 * @a: ints
 * @b: ints
 * Return: result
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - adds
 * @a: ints
 * @b: ints
 * Return: result
 */
int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - adds
 * @a: ints
 * @b: ints
 * Return: result
 */
int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - adds
 * @a: ints
 * @b: ints
 * Return: result
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - adds
 * @a: ints
 * @b: ints
 * Return: result
 */
int op_mod(int a, int b)
{
	return (a % b);
}



